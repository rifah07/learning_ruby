#  Ruby has an Exception class that makes handling these errors much easier. It also has a syntactic structure using the reserved words begin,
#  rescue, and end to signify exception handling. The basic structure looks like this.


begin
  # perform some dangerous operation
rescue StandardError
  # do this if operation fails
  # for example, log the error
end

# Exception handling is a structure used to handle the possibility of an error occurring in a program.
# It is a way of handling the error by changing the flow of control without exiting the program entirely. - The LAUNCH SCHOOL
